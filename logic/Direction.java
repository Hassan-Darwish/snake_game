package snake_game.logic;

/******************************************************************************
 *  ENUM NAME    : Direction
 *  DESCRIPTION  : Enumerates possible movement directions for the snake.
 *                 NULL indicates no movement.
 ******************************************************************************/
public enum Direction 
{
    UP,    // Move upward on the board
    DOWN,  // Move downward on the board
    LEFT,  // Move leftward on the board
    RIGHT  // Move rightward on the board
}

